// FUNCTION      : displayRunTime()
// DESCRIPTION   : This function takes the run time and displays
//				   it on the LCD
// PARAMETERS    : totalRunTime - structure pointer of TIME
// RETURNS       : Nothing
void displayRunTime(TIME* totalRunTime)
{
	char stringBuffer[16] = {0};
	HD44780_GotoXY(0, 0); // move to First line first position
	snprintf(stringBuffer, 16, "Run Time:");
	HD44780_PutStr(stringBuffer);
	HD44780_GotoXY(0, 1); // move to second line first position
	snprintf(stringBuffer, 16, "%i:%i:%i.%i",
			totalRunTime->hour, totalRunTime->minute, totalRunTime->second,
			totalRunTime->milliSecond);
	HD44780_PutStr(stringBuffer);
}

// FUNCTION      : convertMilliSecToHHMMSS()
// DESCRIPTION   : Takes time in millisecond and convert it to hour
//				   minute second and millisecond.
// PARAMETERS    : msTotalRunTime - (in32_t) this is the total run time
//				   in millisecond
//				   totalRunTime - structure pointer of TIME
// RETURNS       : Nothing
void convertMilliSecToHHMMSS(int32_t msTotalRunTime, TIME* totalRunTime)
{
	totalRunTime->second = msTotalRunTime / 1000;
	totalRunTime->milliSecond = msTotalRunTime % 1000;
	totalRunTime->minute = totalRunTime->second / 60;
	totalRunTime->second %= 60;
	totalRunTime->hour = totalRunTime->minute / 60;
	totalRunTime->minute %= 60;
}

// FUNCTION      : measureRunTime()
// DESCRIPTION   : Measures the run time in millisecond,
//				   converts it to HH:MM:SS.SS format and displays it on LCD
// PARAMETERS    : pbPressed - in8_t - 0 if push button pressed 1 otherwise
//				   count - int32_t - the countvalue that denotes howmany millisecond
// RETURNS       : nothing
int32_t measureRunTime(int8_t pbPressed, int32_t count, int32_t* msTotalRunTime,
		TIME* totalRunTime)
{
	if (pbPressed == 0)
	{
		count++;
		HAL_Delay(1); // This Delay ensures that the count gets
		// incremented every millisecond
	}
	else
	{
		*msTotalRunTime = *msTotalRunTime + count;
		count = 0;
		printf("Total Run Time:%i\r\n", *msTotalRunTime);
		displayRunTime(&*totalRunTime);
		convertMilliSecToHHMMSS(*msTotalRunTime, &*totalRunTime);
	}
	return count;
}

